    private void recordAllbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recordAllbtnActionPerformed
        // TODO add your handling code here:
        File outFile;
        FileWriter fw;
        BufferedWriter bw;

        String input = (numtf.getText());
        String input1 = (numtf1.getText());
        String input2 = (numtf2.getText());
        String input3 = (numtf3.getText());
        String input4 = (numtf4.getText());

        try {

            outFile = new File("FirstPhase.txt");

            fw = new FileWriter(outFile, true);
            bw = new BufferedWriter(fw);

            bw.write(input);
            bw.write(input1);
            bw.write(input2);
            bw.write(input3);
            bw.write(input4 + ".....//");
            bw.newLine();

            bw.close();

        } catch (IOException e) {
            errorDisplay.setText("Error writing to file" + e);
            System.out.println("Error writing to file" + e);
        }
        status.setText("you file was created");

    }//GEN-LAST:event_recordAllbtnActionPerformed

