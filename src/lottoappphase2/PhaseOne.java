    private void FinishbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinishbtnActionPerformed
         // Input one
        for (int r = 0; r < rand.length; r++) {
            input1 = Integer.parseInt(numtf.getText());
            if (input1 == rand[r]) {
                guess++;
            }
        }

        //Input two
        for (int r = 0; r < rand.length; r++) {
            input2 = Integer.parseInt(numtf1.getText());
            if (input2 == rand[r]) {
                guess++;
            }
        }

        //Input three
        for (int r = 0; r < rand.length; r++) {
            input3 = Integer.parseInt(numtf2.getText());
            if (input3 == rand[r]) {
                guess++;
            }
        }

        //Input four
        for (int r = 0; r < rand.length; r++) {
            input4 = Integer.parseInt(numtf3.getText());
            if (input4 == rand[r]) {
                guess++;
            }
        }

        //input five
        for (int r = 0; r < rand.length; r++) {
            input5 = Integer.parseInt(numtf4.getText());
            if (input5 == rand[r]) {
                guess++;
            }
        }

        outputar.setText("you guessed " + guess + " right");

        Finishbtn.setEnabled(false);
        resetbtn.setEnabled(true);

    }//GEN-LAST:event_FinishbtnActionPerformed

